class Product {
  final String id;
  final String name;
  final double price;
  final int sales;
  final String description;
  final List<String> images;
  final String category;
  final String brand;
  final double rating;
  Product({
    required this.id,
    required this.name,
    required this.price,
    required this.sales,
    required this.description,
    required this.images,
    required this.category,
    required this.brand,
    required this.rating,
  });
}